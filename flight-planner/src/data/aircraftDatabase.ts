/**
 * Get all aircraft
 */
export function getAllEnhancedAircraft(): EnhancedAircraft[] {
  return Object.values(aircraftDatabase);
}

/**
 * Get aircraft by ICAO code
 */
export function getAircraftByCode(icaoCode: string): EnhancedAircraft | undefined {
  return aircraftDatabase[icaoCode.toUpperCase()];
}

/**
 * Search aircraft by name, code, or manufacturer
 */
export function searchEnhancedAircraft(query: string): EnhancedAircraft[] {
  const upper = query.toUpperCase();
  return Object.values(aircraftDatabase).filter(aircraft =>
    aircraft.icaoCode.includes(upper) ||
    aircraft.name.toUpperCase().includes(upper) ||
    aircraft.manufacturer.toUpperCase().includes(upper)
  );
}

/**
 * Filter aircraft by wake turbulence category
 */
export function getAircraftByWakeCategory(category: 'L' | 'M' | 'H' | 'J'): EnhancedAircraft[] {
  return Object.values(aircraftDatabase).filter(
    aircraft => aircraft.wakeTurbulenceCategory === category
  );
}

/**
 * Get aircraft suitable for a specific runway length
 */
export function getAircraftForRunwayLength(lengthMeters: number): EnhancedAircraft[] {
  return Object.values(aircraftDatabase).filter(
    aircraft => 
      aircraft.runwayRequirements.minTakeoffRunway <= lengthMeters &&
      aircraft.runwayRequirements.minLandingRunway <= lengthMeters
  );
}
